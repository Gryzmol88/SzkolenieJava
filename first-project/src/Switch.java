public class Switch {
    public static void main(String[] args) {
        int a = 2;

        switch (a) {
            case 1:
                System.out.println("To jest 1!");
                break;
            case 2:
                System.out.println("To jest 2");
                break;
            case 3:
                System.out.println("To jest 3");
                break;
            default:
                System.out.println("COs na koncu");
                break;
        }
    }
}
